    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        Connection cone = Conex.getInstance().getConnection();
        String cadSha1;
        Statement stm;
        try {
            stm = cone.createStatement();
            String cadena = new String(txtPassword.getPassword());
            cadena = Hash.sha1(cadena);
            ResultSet rs = stm.executeQuery("select * from usuarios where clave='" +txtClave.getText() + "' and password= '"+ cadena+"'");
            if(rs.next()){
                Datos.idusuario=rs.getInt("idusuario");
                Datos.nombre = rs.getString("nombre");
                Datos.modPac = rs.getInt("mpacientes");
                Datos.modFarm = rs.getInt("mfarmacia");
                Datos.modControlEnf = rs.getInt("mctrlenf");
                Datos.modbenefa = rs.getInt("mbenefactores");
                Datos.modInventario = rs.getInt("minventario");   
                Datos.modUsuarios = rs.getInt("musuarios");
                FrmPrincipal ppal = new FrmPrincipal();
                ppal.setVisible(true);
                UsuarioActual.setUsuarioActual(txtClave.getText());
                System.out.println("Login: "+UsuarioActual.getUsuarioActual());
                this.setVisible(false);
            } else{
                JOptionPane.showMessageDialog(this, "usuario o Password inv√°lido");
            }           
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(FrmLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
       
            
    }//GEN-LAST:event_btnLoginActionPerformed

